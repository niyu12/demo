package com.Tvt.BEAN;


public class BlobBean {

	private String BlobName=null;
	private String BucketName=null;
	private String BlobInfo=null;
	private String BlobConetnt=null;
	
	
	
	public String getBlobName() {
		return BlobName;
	}
	public void setBlobName(String blobName) {
		BlobName = blobName;
	}
	public String getBucketName() {
		return BucketName;
	}
	public void setBucketName(String bucketName) {
		BucketName = bucketName;
	}
	public String getBlobInfo() {
		return BlobInfo;
	}
	public void setBlobInfo(String blobInfo) {
		BlobInfo = blobInfo;
	}
	public String getBlobConetnt() {
		return BlobConetnt;
	}
	public void setBlobConetnt(String blobConetnt) {
		BlobConetnt = blobConetnt;
	}
	
	
	
}
